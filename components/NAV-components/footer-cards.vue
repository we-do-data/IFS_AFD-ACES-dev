import { mapState, mapGetters, mapMutations, mapActions } from 'vuex'
// import { InteractEventBus } from 'vue2-interact'

import { EVENTS, INTERACT_EVENTS } from "~/config/interactEvents.js"


export default {

  name: "FooterCards",

  components: {
  },

  props: [
  ],

  beforeMount: function() {
    console.log("C-FooterCards / beforeMount....")
  },

  data() {

    return {

      footerBtnsLeft : [
        { textCode: "cards.previous", icon: "fas fa-undo-alt", to: "/previous" },
      ],

      footerBtnsCenter: [
        { textCode: "twitter", icon: "fab fa-twitter", to: "/about" },
        { textCode: "facebook", icon: "fab fa-facebook", to: "/credits" },
        { textCode: "screenshot", icon: "fas fa-camera", to: "/credits" },
      ],

      footerBtnsRight : [
        { textCode: "cards.next", icon: "fas fa-arrow-right", to: "/next" },
      ],

      showNextBreakpoints : [ 'xl' ],
      
      // interactEventBus: {
      //   draggedRight: EVENTS.SKIP,
      //   draggedLeft: EVENTS.SKIP,
      //   draggedUp: EVENTS.SKIP
      // },

    }

  },

  created() {
    window.addEventListener('resize', this.handleResize)
    this.handleResize()
  },
  destroyed() {
    window.removeEventListener('resize', this.handleResize)
  },

  computed: {

    ...mapState({
      log : state => state.log, 
      locale : state => state.locale,
      locSelected : state => state.locSelected,

      cardWindow : state => state.cardWindow,

      index : state => state.cards.currentCardIndex,

    }),

    ...mapGetters({
    }),

    showNext(){
      let screenBreakPoint = this.$vuetify.breakpoint.name
      return this.showNextBreakpoints.includes(screenBreakPoint)
    }
  },

  methods: {

    handleResize() {

      // if ( needInvertAndroid  ) {
      //   this.cardWindow.width = window.innerHeight
      //   this.cardWindow.height = window.innerWidth
      // } else {
      //   this.cardWindow.width = window.innerWidth
      //   this.cardWindow.height = window.innerHeight
      // }

      // this.cardWindow.width = window.innerWidth
      // this.cardWindow.height = window.innerHeight

      let currentWindow = { 
        width : window.innerWidth,
        height : window.innerHeight
      }
      this.$store.commit('setCardWindow', currentWindow )
    },

    ...mapMutations({
      setCurrentCardIndex: 'cards/setCurrentCardIndex',
      setPreviousCurrentCardIndex : 'cards/setPreviousCurrentCardIndex'
    }),

    cardWidth ( widthPercent ) {
      // let maxWidth = 100
      // let zWidth = maxWidth * widthPercent 
      let step = .1
      switch (this.$vuetify.breakpoint.name) {

        // case 'xs': return zWidth + 'vw'
        // case 'sm': return ( zWidth - (step * 4) ) + 'vw'
        // case 'md': return ( zWidth - (step * 5) ) + 'vw'
        // case 'lg': return ( zWidth - (step * 6) ) + 'vw'
        // case 'xl': return ( zWidth - (step * 7) ) + 'vw'

        case 'xs': return Math.round(( widthPercent * this.cardWindow.width )) + 'px'
        case 'sm': return Math.round(( ( widthPercent - (step * 4) ) * this.cardWindow.width )) + 'px'
        case 'md': return Math.round(( ( widthPercent - (step * 5) ) * this.cardWindow.width )) + 'px'
        case 'lg': return Math.round(( ( widthPercent - (step * 6) ) * this.cardWindow.width )) + 'px'
        case 'xl': return Math.round(( ( widthPercent - (step * 7) ) * this.cardWindow.width )) + 'px'
      }
    },

    skip( isNext=true ) {
      console.log("C-FooterCards-skip / isNext : ", isNext )
      // console.log("C-FooterCards-skip ... InteractEventBus : \n", InteractEventBus )

      if ( isNext ){
        // swipe to next
        // InteractEventBus.$emit(INTERACT_EVENTS.INTERACT_DRAGGED_RIGHT);
        this.$bus.$emit(INTERACT_EVENTS.INTERACT_DRAGGED_RIGHT);
        // this.$bus.$emit('skip');
      } else {
        // reload previous
        // InteractEventBus.$emit(INTERACT_EVENTS.INTERACT_DRAGGED_LEFT);
        // this.$emit('reloadPreviousItem')
        this.setPreviousCurrentCardIndex()
      }

    },

  },

}
